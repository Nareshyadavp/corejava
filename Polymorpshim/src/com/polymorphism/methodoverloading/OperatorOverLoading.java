package com.polymorphism.methodoverloading;

// one operator with  morethan one behaviour  // implicity is + operator  // 
public class OperatorOverLoading {

	public static void main(String[] args) {

		// operator overloadin
		System.out.println(10 + 20);
		System.out.println("rathan" + "ramu");// concatination
		System.out.println(10 + "rathan" + "ramu" +(10 + 20));// concatination
		System.out.println("ranthan+ramu");

	}

}

// operator overloading take care of the jvm
// java is not supports operator overloading